# wiz3D - generate_version.ps1
#
# Reads the repo-root VERSION file (MAJOR.MINOR.PATCH), the current git short
# SHA, and the build date, then writes bin/temp_version.h. That header is
# included by Shared/version.h and ultimately ends up in every DLL's
# VERSIONINFO resource.
#
# Called from Preparing.vcxproj as a pre-build step. Fails the build (exit 1)
# if VERSION can't be parsed.

# Locate the repo root (this script lives in <repo>/bin/, so parent of $PSScriptRoot)
$repoRoot = Split-Path -Parent $PSScriptRoot
$versionFile = Join-Path $repoRoot 'VERSION.txt'
$outFile = Join-Path $PSScriptRoot 'temp_version.h'

if (-not (Test-Path $versionFile)) {
    Write-Error "VERSION.txt not found at: $versionFile"
    exit 1
}
# Note: file is VERSION.txt (not bare VERSION) because the repo root is on the
# C++ include path via Common.props, and a bare VERSION would shadow the C++20
# <version> standard header on case-insensitive filesystems (Windows). Boost
# headers #include <version>; without the .txt extension, MSVC resolves the
# include to our text file and the build fails with C2059 errors.

# Parse "MAJOR.MINOR.PATCH" — ignore trailing whitespace/newlines, allow optional
# pre-release suffix like "0.1.8-rc1" (suffix dropped for the numeric components)
$raw = (Get-Content -Raw $versionFile).Trim()
$numeric = $raw -split '-', 2 | Select-Object -First 1
$parts = $numeric -split '\.'

if ($parts.Count -lt 3) {
    Write-Error "VERSION file must contain MAJOR.MINOR.PATCH (got: '$raw')"
    exit 1
}

$major = [int]$parts[0]
$minor = [int]$parts[1]
$patch = [int]$parts[2]

# git short SHA — fall back to "unknown" if git isn't available or the working
# tree isn't a repo (e.g. source tarball builds)
$gitSha = 'unknown'
try {
    Push-Location $repoRoot
    $sha = (& git rev-parse --short HEAD 2>$null).Trim()
    if ($LASTEXITCODE -eq 0 -and $sha) { $gitSha = $sha }
    # Append "-dirty" if working tree has uncommitted changes
    & git diff --quiet 2>$null
    if ($LASTEXITCODE -ne 0) { $gitSha = "$gitSha-dirty" }
} catch {
    # git unavailable; leave $gitSha = 'unknown'
} finally {
    Pop-Location
}

$buildDate = (Get-Date -Format 'yyyy-MM-dd')
$buildTime = (Get-Date -Format 'HH:mm:ss')

# Generate the header. Note: VERSION_BUILD_DATE / VERSION_BUILD_TIME without
# the WIZ3D_ prefix are referenced by the inherited iZ3D Shared/version.h.
$content = @"
// AUTO-GENERATED by bin/generate_version.ps1 -- do not edit by hand.
// Source: VERSION file at repo root + git short SHA + build timestamp.
#pragma once

#define WIZ3D_VERSION_MAJOR  $major
#define WIZ3D_VERSION_MINOR  $minor
#define WIZ3D_VERSION_PATCH  $patch
#define WIZ3D_VERSION_QFE    0
#define WIZ3D_GIT_SHA        "$gitSha"
#define WIZ3D_BUILD_DATE     "$buildDate"
#define WIZ3D_BUILD_TIME     "$buildTime"

// Legacy iZ3D macro aliases (consumed by Shared/version.h and per-DLL
// Output.rc / version.h chains). The #undef before each #define
// silences C4005 redefinition warnings: per-DLL version.ver files predate this
// header in the include order and assign their own (now-stale) values; we
// override them so all DLLs share the unified wiz3D version.
#undef  PRODUCT_VERSION_MAJOR
#define PRODUCT_VERSION_MAJOR  WIZ3D_VERSION_MAJOR
#undef  PRODUCT_VERSION_MINOR
#define PRODUCT_VERSION_MINOR  WIZ3D_VERSION_MINOR
#undef  PRODUCT_VERSION_BUILD
#define PRODUCT_VERSION_BUILD  WIZ3D_VERSION_PATCH
#undef  PRODUCT_VERSION_QFE
#define PRODUCT_VERSION_QFE    WIZ3D_VERSION_QFE
#undef  VERSION_MAJOR
#define VERSION_MAJOR          WIZ3D_VERSION_MAJOR
#undef  VERSION_MINOR
#define VERSION_MINOR          WIZ3D_VERSION_MINOR
#undef  VERSION_BUILD
#define VERSION_BUILD          WIZ3D_VERSION_PATCH
#undef  VERSION_QFE
#define VERSION_QFE            WIZ3D_VERSION_QFE
#undef  VERSION_BUILD_DATE
#define VERSION_BUILD_DATE     WIZ3D_BUILD_DATE
"@

# Only rewrite if content changed — avoids triggering unnecessary rebuilds.
$existing = if (Test-Path $outFile) { Get-Content -Raw $outFile } else { $null }
if ($content -ne $existing) {
    # Write as ASCII (no BOM) -- MSVC's preprocessor handles BOM-less ASCII
    # most reliably when this header is #include'd into .rc files.
    # Append a trailing newline: rc.exe reports RC1004 "unexpected end of file"
    # if the .h ends mid-line (here-strings don't auto-terminate the last line).
    [System.IO.File]::WriteAllText($outFile, $content + "`r`n", [System.Text.Encoding]::ASCII)
    Write-Host "wiz3D version: $major.$minor.$patch ($gitSha)  -> $outFile"
} else {
    Write-Host "wiz3D version: $major.$minor.$patch ($gitSha)  (temp_version.h unchanged)"
}

# Explicit success. The `git diff --quiet` dirty-check above exits 1 on a dirty
# tree, leaving $LASTEXITCODE = 1; pwsh would otherwise propagate that as the
# script's process exit code and fail the build step even though we succeeded.
exit 0
